const languageNames: Record<Language, string> = {
  zh: '中文',
  en: 'English',
  ja: '日本語',
};

function detectLanguage(): Language {
  if (typeof window !== 'undefined') {
    try {
      const saved = window.localStorage.getItem(languageStorageKey);
      if (saved === 'zh' || saved === 'en' || saved === 'ja') return saved;
    } catch {
      // Local storage is best effort in embedded webviews.
    }
  }
  const locale = typeof navigator === 'undefined' ? '' : navigator.language.toLowerCase();
  if (locale.startsWith('ja')) return 'ja';
  if (locale.startsWith('zh')) return 'zh';
  return 'en';
}

type I18nContextValue = {
  language: Language;
  languageNames: Record<Language, string>;
  setLanguage: (language: Language) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(detectLanguage);
  function setLanguage(language: Language) {
    setLanguageState(language);
    try {
      window.localStorage.setItem(languageStorageKey, language);
    } catch {
      // Local storage is best effort in embedded webviews.
    }
  }
  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      languageNames,
      setLanguage,
      t: (key: string) => dictionaries[language][key] || dictionaries.en[key] || key,
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('I18nProvider is required');
  }
  return context;
}

